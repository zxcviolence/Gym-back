const mongoose = require("mongoose");

const massageSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

const Massage = mongoose.model("massage", massageSchema);

module.exports = Massage;
